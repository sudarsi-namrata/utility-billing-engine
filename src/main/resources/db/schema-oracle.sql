-- Materialized view for billing summary reporting
CREATE MATERIALIZED VIEW MV_BILLING_SUMMARY
BUILD IMMEDIATE REFRESH ON DEMAND AS
SELECT
    a.SERVICE_CLASS,
    a.BILLING_CYCLE,
    COUNT(DISTINCT a.ID) as ACCOUNT_COUNT,
    SUM(b.TOTAL_AMOUNT) as TOTAL_BILLED,
    SUM(b.AMOUNT_PAID) as TOTAL_COLLECTED,
    SUM(b.BALANCE_DUE) as TOTAL_OUTSTANDING
FROM ACCOUNTS a
LEFT JOIN BILLS b ON a.ID = b.ACCOUNT_ID
GROUP BY a.SERVICE_CLASS, a.BILLING_CYCLE;
